l1 = [3, [66, 55, 44], (7, 8, 9)]
l2 = l1[:]
l1.append(100)
l1[1].remove(55)
print('l1:', l1)
print('l2:', l2)
l1[1] += [33, 22]
l2[2] += (10, 11)
print('l1:', l1)  # l1: [3, [66, 44, 33, 22], (7, 8, 9), 100]
print('l2:', l2)  # l2: [3, [66, 44, 33, 22], (7, 8, 9, 10, 11)]
# 对列表是原地修改列表，对元组+=是创建新元组，因此两个结果不同
# 对于 += 和 *= 等增量运算符 如果左边是可变对象，会就地修改，如果左边是不可变对象，则会创建新对象
